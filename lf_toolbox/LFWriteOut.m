% The purpose of this program is to display the sub aperture images pulled from the 
% original lf image. 

function LFWriteOut(LF, writeDir)
    delete(strcat(writeDir, '/*.png');
    pxStart = 6;
    pxEnd = 20;
    pxDist = 1;
    nCols = pxEnd - pxStart;
    nRows = nCols;

    icounter = 1;
    jcounter = 1;
    
end
